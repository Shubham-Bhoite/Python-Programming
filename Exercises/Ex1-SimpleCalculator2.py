X = 50
Y = 4

print("The value of", X, "+", Y, "is: ", X + Y)
print("The value of", X, "-", Y, "is: ", X - Y)
print("The value of", X, "*", Y, "is: ", X * Y)
print("The value of", X, "/", Y, "is: ", X / Y)
print("The value of", X, "//", Y, "is: ", X // Y)
print("The value of", X, "%", Y, "is: ", X % Y)
print("The value of", X, "**", Y, "is: ", X ** Y)